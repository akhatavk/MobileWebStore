package com.cmpe281.project.beans;

public class AccessoriesBean {
	int accessoryId;
	int mobileId;
	int cover;
	int headSet;
	int screenGuard;
	int dataCable;

	public int getAccessoryId() {
		return accessoryId;
	}

	public void setAccessoryId(int accessoryId) {
		this.accessoryId = accessoryId;
	}

	public int getMobileId() {
		return mobileId;
	}

	public void setMobileId(int mobileId) {
		this.mobileId = mobileId;
	}

	public int getCover() {
		return cover;
	}

	public void setCover(int cover) {
		this.cover = cover;
	}

	public int getHeadSet() {
		return headSet;
	}

	public void setHeadSet(int headSet) {
		this.headSet = headSet;
	}

	public int getScreenGuard() {
		return screenGuard;
	}

	public void setScreenGuard(int screenGuard) {
		this.screenGuard = screenGuard;
	}

	public int getDataCable() {
		return dataCable;
	}

	public void setDataCable(int dataCable) {
		this.dataCable = dataCable;
	}

}
